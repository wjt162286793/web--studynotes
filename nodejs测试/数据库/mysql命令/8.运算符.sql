#1.算数运算符

#加法运算符和减法运算符
SELECT 100, 100+1,100+100,100-1,100-100,100-200 FROM DUAL;

+-----+---------+---------+----------+--------------+
| 100 | 100+1 | 100+100 | 100-1 | 100-100 | 100-200 | 
+-----+---------+---------+----------+--------------+
| 100 |   101 |   200   |   99  |    0    |  -100   |
+-----+---------+---------+----------+--------------+

#乘法运算符和除法运算符
SELECT 100*2,100*-2,100*1.5,100/2,100/-2 FROM DUAL;

#求余运算符

SELECT 11%3,12 MOD 5 FROM DUAL;

+--------+-----------+
|  11%3  | 12 MOD 5  |
+--------+-----------+
|   2    |     2     |
+--------+-----------+

#2.比较运算符
#符合为1,不符合为0,代表true和false
#  =        等于运算符
#  <=>      安全等于运算符
#  <> !=    不等于运算符
#  <        小于运算符
#  <=       小于等于运算符
#  >        大于运算符
#  >=       大于等于运算符

#等于
SELECT 10 = 1+9 , 10 = 11- 2 FROM DUAL;
+------------+-----------+
|  10 = 1+9  | 12 MOD 5  |
+------------+-----------+
|   1        |     0     |
+------------+-----------+

#不等于
SELECT 10<>1+9,10!=11-2 FROM DUAL;
+------------+-----------+
|  10<>1+9   | 10!=11-2  |
+------------+-----------+
|   0        |     1     |
+------------+-----------+

#大于小于加等于
SELECT 100<101,100>102,100<=100-2,100>=50+50 FROM DUAL;
+------------+-----------+------------+-----------+
|    100<101 |   100>102 | 100<=100-2 |100>=50+50 |
+------------+-----------+------------+-----------+
|    1       |      0    |     0      |   1       |
+------------+-----------+------------+-----------+


#空运算符
SELECT NULL IS NULL ,ISNULL(NULL),ISNULL('2'),10 IS NULL;

+--------------+---------------+---------------+---------------+
| NULL IS NULL | ISNULL(NULL)  |  ISNULL('2')  |   10 IS NULL  |
+--------------+---------------+---------------+---------------+
|     1        |         1     |       0       |     0         |
+--------------+---------------+---------------+---------------+
#非空运算符
SELECT NULL IS NOT NULL,'a' IS NOT NULL,1 IS NOT NULL;

+-------------------+-------------------+------------------+
|  NULL IS NOT NULL |   'a' IS NOT NULL |  1 IS NOT NULL   |
+-------------------+-------------------+------------------+
|        0          |         1         |       1          | 
+-------------------+-------------------+------------------+


#最小值运算符
SELECT LEAST(1,3,5),LEAST('A','B','C','a','b','c'),LEAST(1,NULL,2);

+-----------------------+--------------------------------+---------------------+
|   LEAST(1,3,5)        | LEAST('A','B','C','a','b','c') |   LEAST(1,NULL,2)   | 
+-----------------------+--------------------------------+---------------------+
|      1                |         A                      |     NULL            |
+-----------------------+--------------------------------+---------------------+


#最大值运算符
SELECT GREATEST(1,3,5),GREATEST('A','B','C','a','b','c'),GREATEST(1,NULL,2);

+-----------------------+-----------------------------------+---------------------+
|   GREATEST(1,3,5)     |GREATEST('A','B','C','a','b','c')  |  GREATEST(1,NULL,2) | 
+-----------------------+-----------------------------------+---------------------+
|      5                |         C                         |    NULL             |
+-----------------------+-----------------------------------+---------------------+

